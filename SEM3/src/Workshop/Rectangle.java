package Workshop;


	public class Rectangle {
	    private double width;
	    private double height;


	    public Rectangle(double width, double height) {
	        this.width = width;
	        this.height = height;
	    }

	
	    @Override
	    public String toString() {
	        return "Rectangle [Width: " + width + ", Height: " + height + "]";
	    }


	    
	}


